import random

def get_chord_rhythm(dur):
    chord_rhythm_options = {
        8: [[8]],
        12:[[12], [8,4], [8,4], [4,4,4]],
        16:[[16], [8,8], [12,4], [8,4,4]],
        
        20:[[12,8],[8,12]],
        24:[[16,8], [8,16], [12,12]],
        28:[[16,12],[12,16],[12,8,8],[8,12,8],[8,8,12]],
        32:[[16,16],[12,12,8],[12,8,12],[8,12,12]],
        36:[[16,12,8],[16,8,12],[12,12,12]],
        40:[[16,16,8],[16,8,16],[8,16,16],[16,12,12],[12,16,12],[12,12,16]],
        44:[[16,16,12],[16,12,16],[12,16,16],[12,12,12,8],[12,12,8,12],[12,8,12,12],[8,12,12,12]],
        48:[[16,16,16],[12,12,12,12]],
        52:[[12,12,12,16],[12,12,16,12],[12,16,12,12],[16,12,12,12],
            [16,16,12,8],[16,16,8,12], [16,12,16,8],[16,12,8,16],
            [16,8,16,12],[16,8,12,16],[12,16,16,8],[12,16,8,16],[12,8,16,16],
            [8,16,16,12],[8,16,12,16],[8,12,16,16]],
        56:[[16,16,16,8],[16,16,8,16],[16,8,16,16],[8,16,16,16],[16,16,12,12],
            [16,12,16,12],[16,12,12,16],[12,16,16,12],[12,16,12,16],[12,12,16,16]]
    }    
    opts = chord_rhythm_options[dur]
    rhythm = random.choice(opts)
    new_rhythm = []
    for d in rhythm:
        if d in chord_rhythm_options:
            new_rhythm.extend(random.choice(chord_rhythm_options[d]))
        else:
            new_rhythm.append(d)
    return new_rhythm
    
